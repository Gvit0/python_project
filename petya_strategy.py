# Определяем, может ли Петя выиграть за один ход, и при каких условиях.
def can_petya_win_in_one_move(first_heap, second_heap):
    # Если сумма количества камней во всех кучах равна 1 (плюс текущие 1), то Петя не может выиграть.
    if first_heap + second_heap == 1:
        return False
    # Если Петя может достичь такой позиции, что у него будет больше камней в одной из куч,
    # и при этом сумма количества камней во всех кучах станет больше 1, то Петя может выиграть.
    elif (first_heap == 1 and second_heap == 2) or (first_heap == 2 and second_heap == 1):
        return True
    # Иначе Петя не может выиграть.
    else:
        return False

# Ищем минимальное значение S, при котором Петя не может выиграть за один ход, но при любом ходе Пети Ваня сможет выиграть своим первым ходом.
for s in range(1, 69):
    if not can_petya_win_in_one_move(12, s):
        print(s)
        break
